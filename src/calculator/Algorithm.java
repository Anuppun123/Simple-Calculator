package calculator;
public class Algorithm {
    public static int addition(int a, int b){
        int sum = a + b;
         return sum;
     }
     public static int substraction(int a, int b){
         int sub = a - b ;
         return sub;
     }
     public static int multiply(int a, int b){
         int mult = a * b;
         return mult;
     }
     public static int divide(int a, int b){
         int div = a/b;
         return div;
     }
}
